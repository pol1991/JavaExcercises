package com.pawelkoper.JavaCwiczenia.zajecia2;

public class TabliczkaMnozenia {
    public static void main(String[] args) {

        for (int i=1;i<=10;i++)
        {
            for (int j=1;j<=10;j++)
            {
                System.out.println(i+" * "+j+ "= "+i*j);
            }
        }
    }
}
